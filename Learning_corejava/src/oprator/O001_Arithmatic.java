package oprator;

public class O001_Arithmatic {
	
	public static void main(String[] args) {
		
		double a = 15;
		double b = 4;
		String name="test";
		
		System.out.println(a+b);
		System.out.println(a+name);
		System.out.println(a+b+name);
		System.out.println(name+a+b);
		
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
	}
	
	
	
}
